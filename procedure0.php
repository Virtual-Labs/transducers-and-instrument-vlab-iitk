                <!--In the Schering Bridge above, the resistance values of resistors R1 and R2 are known, while the resistance value of resistor R3 is unknown.  The capacitance values of C1 and C2 are also known, while the capacitance of C3 is the value being measured.  To measure R3 and C3, the values of C2 and R2 are fixed, while the values of R1 and C1 are adjusted until the current through the ammeter between points A and B becomes zero.  This happens when the voltages at points A and B are equal, in which case the bridge is said to be 'balanced'. <br> 
When the bridge is balanced, Z1/C2 = R2/Z3, where Z1 is the impedance of R1 in parallel with C1 and Z3 is the impedance of R3 in series with C3. In an AC circuit that has a capacitor, the capacitor contributes a capacitive reactance to the impedance. The capacitive reactance of a capacitor C is 1/2πfC.<br> 
   
As such, Z1 = R1/[2πfC1((1/2πfC1) + R1)] = R1/(1 + 2πfC1R1) while Z3 = 1/2πfC3 + R3.  Thus, when the bridge is balanced: <br> 
2πfC2R1/(1+2πfC1R1) = R2/(1/2πfC3 + R3); or<br> 
2πfC2(1/2πfC3 + R3) = (R2/R1)(1+2πfC1R1); or<br> 
C2/C3 + 2πfC2R3 = R2/R1 + 2πfC1R2.<br> 
    
When the bridge is balanced, the negative and positive reactive components are equal and cancel out, so<br> 
2πfC2R3 = 2πfC1R2 or <br> 
R3 = C1R2 / C2.<br> 
Similarly, when the bridge is balanced, the purely resistive components are equal, so<br> 
