				<h3 align="left" class="title">Inductive Displacement Transducer: - </h3>           
				
				
				
				<div class="entry">
					 <p><b>L.V.D.T.: - </b>The letters LVDT are an acronym for<font color="#0000FF"> Linear Variable Differential Transformer</font>, a common type
of electromechanical transducer that can convert the rectilinear motion of an object to which it is coupled mechanically into a corresponding electrical signal. LVDT linear position sensors are readily available that can measure movements as small as a few millionths of an inch up to several inches, but are also capable of measuring positions up to ±20 inches (±0.5 m). Figure shows the components of a typical LVDT. The transformer's internal structure consists of a primary winding centered between a pair of identically wound secondary windings, symmetrically spaced about the primary. The coils are wound on
a one-piece hollow form of thermally stable glass reinforced polymer, encapsulated against moisture,
wrapped in a high permeability magnetic shield, and then secured in a cylindrical stainless steel housing. This coil assembly is usually the stationary element of the position sensor. The moving element of an LVDT is a separate tubular armature of magnetically permeable material called the core, which is free to move axially within the coil's hollow bore, and mechanically coupled to the object whose position is being measured. This bore is typically large enough to provide substantial radial clearance between the core and bore, with no physical contact between it and the coil. In operation, the LVDT's primary winding is energized by alternating current of appropriate amplitude and frequency, known as the primary excitation. The LVDT's electrical output signal is the differential AC voltage between the two secondary windings, which varies with the axial position of the core within the LVDT coil. Usually this AC output voltage is converted by suitable electronic circuitry to high level DC voltage or current that is more convenient to use.</p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<div align="center"><img src="images/lvdtconstructiontheory.bmp" alt=""/><br/><font color="#000000">Cross sectional view of L.V.D.T.</font></div><br/>
<p><strong><font color="#000000">Working Principle of LVDT&nbsp;&nbsp;&nbsp;:</font></strong>
Figure illustrates what happens when the LVDT's core is in different axial positions. The LVDT's primary winding, P, is energized by a constant amplitude AC source. The magnetic flux thus developed is coupled by the core to the adjacent secondary windings, S1 and S2. If the core is located midway between S1 and S2, equal flux is coupled to each secondary so the voltages,
E1 and E2, induced in windings S1 and S2 respectively, are equal. At this reference midway core position, known as the null point, the differential voltage output, (E1 - E2), is essentially zero. As shown in Figure 2, if the core is moved closer to S1 than to S2, more flux is coupled to S1 and less to S2, so the induced voltage E1 is increased while E2 is decreased, resulting in the differential voltage (E1 - E2). Conversely, if the core is moved closer to S2, more flux is coupled to S2 and less to S1, so E2 is increased as E1 is decreased, resulting in the differential voltage (E2 - E1).</p>
