				<h3 class="style4"><font color="#000000">2.	Voltage Current Characteristics  </font></h3><p>
					 <p> The voltage drop across a thermistor increases with increasing current until it reaches a peak value beyond which the voltage drop decreases as the current increases. In this portion of the curve, the thermistor exhibits a negative resistance characteristic. If a very small voltage is applied to the thermistor, the resulting small current does not produce sufficient heat to raise the temperature of the thermistor above ambient. Under this condition, Ohm's law is followed and the current is proportional to the applied voltage. Larger currents, at larger applied voltages, produce enough heat to raise the thermistor temperature above the ambient temperature and its resistance then decreases. As a result, more current is then drawn and the resistance decreases further. The current continues to increase until the heat dissipation of the thermistor equals the power supplied to it. Therefore, under any fixed ambient conditions, the resistance of a thermistor is largely a function of the power being dissipated within itself, provided that there is enough power available to raise its temperature above ambient. Under such operating conditions, the temperature of the thermistor may rise 100 <sup>0</sup>C or 200 <sup>0</sup>C and its resistance may drop to one-thousandth of its value at low current.<br>
					 This characteristic of self-heating provides an entirely new field of uses for the thermistor. In the self-heat state, the thermistor is sensitive to anything that changes the rate at which heat is conduced away from it. It can so be used to measure flow, pressure, liquid level, composition of gases, etc. If, on the other hand, the rate of heat removal is fixed, then the thermistor is sensitive to power input and can be used for voltage or power-level control. 
					 
 </p>
			         <h3 class="style4"><font color="#000000">Applications of Thermistors  </font></h3><p><P> Although major applications of thermistors are measurement and control of temperature, they may be used for a number of other applications. The various applications of thermistors are: 

 </p>
			
			<h3 class="style4"><font color="#000000">1. Measurement of Temperature:    </font></h3><p><p>A thermistor produces a large change of resistance with a small change in the temperature being measured. This large sensitivity of thermistor provides good accuracy and resolution. A typical industrial-type thermistor with a 2000W  resistance at 25 <sup>0</sup>C and a resistance temperature co-efficient of 3.9 percent per <sup>0</sup>C exhibits a change of 78W per <sup>0</sup>C  change is temperature. When this thermistor is connected in a simple series circuit consisting of a battery and micro-ammeter , any change in temperature causes a change in the resistance of thermistor and corresponding change in the circuit current. The micro-ammeter may be directly calibrated in terms of temperature. The micro-ammeter may be able to give a resolution of 0.1<sup>0</sup>C.<br>
				The high sensitivity together with high thermistor resistance which may be about 100W , makes the thermistor ideal for remote measurement or control, as the changes in contact or transmission line resistances due to change in the ambient temperature have almost a negligible effect on the accuracy of measurement or control. For example a 150 m long transmission line made of copper when subjected to change of 25<sup>0</sup>C will affect the accuracy of measurement or control by approximately 0.05<sup>0</sup>C.  

            <h3 class="style4"><font color="#000000">2.	Control of Temperature   </font></h3><p><p>A simple temperature control circuit may be constructed by replacing the micro-ammeter shown in the typical thermistor temperatue control circuit of  with a relay.  It uses a 4KW thermistor connected in an a.c. excited bridge. The unbalance voltage is fed to an a.c. amplifier whose output excites a relay coil. The relay contacts are used to control the current in the circuit which generates the heat. These circuits can be controlled to a precision of 0.00005<sup>0</sup>C . <br>
Thermistor control systems are inherently sensitive, stable and fast acting and require relatively simple circuitry. The voltage output of the standard bridge circuit at 25<sup>0</sup>C is about 18mV/ <sup>0</sup>C using a 4KW  thermistor in the configuration .<br>
<h3 class="style4"><font color="#000000">3.	Temperature Compensation  </font></h3><p><p>Because thermistors have a negative temperature coefficient of resistance opposite to the positive coefficient of most electrical conductors and semiconductors they are widely used to compensate for the effects of temperature on both component and circuit performance. Disk-type thermistors are used for this purpose where the maximum temperature does not exceed 125<sup>0</sup>C . A properly selected thermistor, mounted against or near a circuit element, such as a copper meter coil, and experiencing the same ambient temperature changes, can be connected in such a way that the total circuit resistance is constant over a wide range of temperatures. <br>
The compensator consists of a thermistor, shunted by a resistor. The negative temperature coefficient of this combination equals the positive coefficient of the copper coil. The coil resistance of 5000W at 25<sup>0</sup>C , varies from approximately 4500W at 0<sup>0</sup>C  to 5700W at 60<sup>0</sup>C , representing a change of about &plusmn; 12 percent. With a single thermistor compensation network, this variation is reduced to about &plusmn;15W or &plusmn;1/4 percent. With double or triple compensation networks, variations can be reduced even further. 


</p>
