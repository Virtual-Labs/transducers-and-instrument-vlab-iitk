				<h1 class="title"><font color="#000000">Introduction</font>  </h1>
				
				<div class="entry">
					 <p>When performing spectral analysis on a high-frequency signal, the application determines the best instrument to use. Two principal types of spectrum analyzers exist, the Fast Fourier Transform (FFT) analyzer and the swept spectrum analyzer. This article presents the basic operation of both types of analyzers and discusses the advantages of each, giving the reader the necessary guidance to use and study the types of spectrum analyzer in detail. </p>
					 <p>                     
					 <h3 class="style4"><font color="#000000">Fast Fourier Transform (FFT)</font></h3></p><br>
					 <p>The Fast Fourier Transform (FFT) is based on the conversion of a time domain waveform to the frequency domain. Joseph Fourier developed the Fourier transform, which converted continuous time domain signals into continuous frequency domain information. The frequency domain information includes magnitude and phase values. However, the FFT analyzer samples discrete points over a certain time interval in the time domain and then records the points digitally. Because the time domain waveform is stored as discrete values, the waveform is not continuous and cannot be converted to the frequency domain using the standard Fourier transform. Instead, another version of the Fourier transform, the discrete Fourier transform (DFT), is used to convert the discrete time domain waveform into a discrete frequency domain spectrum.

Because the DFT uses discrete values to transform the time domain waveform into the frequency domain, the frequency domain is divided up into discrete frequency components, or "bins." The number of frequency bins over a given range, called the frequency resolution, is dependent on the number of discrete time domain samples taken. Thus, in order to boost frequency resolution, the number of samples must increase. A limiting factor here is execution time. The more samples that are taken to increase the resolution, the longer it takes to compute the DFT. The FFT is simply a faster method of calculating the DFT. It requires that the time domain waveform contain a number of samples equal to some power of two.</p>
					 <p><div align="center"><img src="images/fo.JPG" alt="" width="561" height="302" /></div><br><div align="center"><font color="#000000">Figure 1</font></div>
					 <h3 class="style4"><font color="#000000">FFT Spectrum Analyzer (FFT)</font></h3></p><br>
					 <p>The FFT analyzer consists of a number of components, each of which is necessary for frequency analysis. First, signals must pass through attenuators or gain stages to adjust the amplitude of the signal to match the input range so as to maximize the resolution of the signal. The signal is passed through an anti-aliasing filter, which we discuss in detail later. Next, a sampler takes discrete points of the analog waveform at a given frequency. This is followed by an analog-to-digital converter (ADC), which converts the samples into digital data. The data is transformed to the frequency domain using the FFT algorithm. The analyzer can use dedicated DSP circuitry to perform the FFT, or the data can be transferred to a PC where the FFT is calculated using software.<br>

The rate at which points are taken by the sampler is a very important parameter of the FFT analyzer. In order for an analog waveform to be represented correctly with discrete values, it must be sampled at a sufficiently high rate. Otherwise the analyzer will interpret the input as a signal of frequency lower than the actual value. According to the sampling theorem, a signal must be sampled at a rate of twice the highest frequency component (the Nyquist rate). Any component whose frequency is higher than the Nyquist rate will appear in the measurement as a lower frequency component. This phenomenon is called aliasing. To avoid aliasing, an analog low-pass filter is placed at the input before the sampler. The low-pass filter determines the highest frequency of the FFT analyzer. Because the rate at which signals can be represented without error is one half the maximum sampling rate, signals are often cut off at a lower frequency to provide sampling rates greater than twice the maximum frequency components. Typically the cutoff of the low-pass filter is 2.5 times less than the maximum sampling rate of the analyzer. This determines the maximum frequency component.<br>

The FFT algorithm is calculated from time records. Each record contains a finite number of discrete points. The FFT converts these time domain points into a finite number of frequency domain points. The frequency domain representation is symmetric about the 0 Hz frequency point. Only the upper half of the transform, which pertains to 0 Hz and above, is kept. Thus, only a certain number of frequencies can be analyzed. The frequency resolution, or spacing between the bins, must be such that the acquired frequencies are represented properly. In order to increase the frequency resolution the number of samples must increase. This is because the frequency resolution is inversely proportional to the length of the time record.

					 
