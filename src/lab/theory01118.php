                  <strong><font color="#000000"><h4>Advantage: -</h4></font></strong>
				<ol>
				  <li>The force requirement is very small, hence the power  required to operate is small and very useful in small system.<strong></strong></li>
				  <li>They are highly sensitive.<strong></strong></li>
				  <li>They have good frequency response and very high  input impedance, so loading effects are minimum.<strong></strong></li>
				  <li> They are  useful in the applications where stray magnetic fields affect performance of  the inductive sensors.</li></ol>
				    <p><strong><font color="#000000">Disadvantage: -</font></strong></p>
				    <ol><li>Proper insulation is required between the metallic  parts of the capacitive transducers.</li>
				      <li>The stray capacitances affect the performance of the  transducer. It can be overcome by proper earthing the frame of the transducer.</li>
				      <li>They show the non-linear behavior due to edge  effects and stray electric field. This can be eliminated by using guard rings.</li>
				      <li>Due to long leads and cables used, loading effect  makes low frequency response poor and reduces sensitivity.</li>
				      <li>For low value capacitances (of the order of  pico-farad) the output impedance tend to very high value which causes loading  effect.</ol>
				        <h4><font color="#000000">Application</font></h4>
				        <font color="#000000"><b><h4>A.</b></font><font color="#000000">Precision positioning:</h4></font>
				        <p>One of the more common applications of capacitive  sensors is for precision positioning. Capacitive displacement sensors can be  used to measure the position of objects down to the nanometer level.  This type of precise positioning is used in the semiconductor industry where silicon  wafers need to be positioned for exposure. Capacitive sensors are also used  to pre-focus the electron microscopes used in testing and  examining the wafers.</p>
				        <h4><font color="#000000">B.Disk Drive Industry</font></h4>				        <p>In the disc drive industry, capacitive  displacement sensors are used to measure the run out of disc drive spindles. By knowing the exact run out of these  spindles, disc drive manufacturers are able to determine the maximum amount of  data that can be placed onto the drives. Capacitive sensors are also used to  ensure that disc drive platters are orthogonal to the spindle before data is written to them.</p>
				        <h4><font color="#000000">C.Precision  thickness measurements:</font></h4>
				        <p>Capacitive displacement sensors can be used to  make very precise thickness measurements. Capacitive displacement sensors operate by measuring changes in  position. If the position of a reference part of known thickness is measured,  other parts can be subsequently measured and the differences in position can be  used to determine the thickness of these parts. In order for this to  be effective using a single probe, the parts must be completely flat and  measured on a perfectly flat surface. If the part to be measured has any curvature or deformity, or simply does not  rest firmly against the flat surface, the distance between the part to be  measured and the surface it is placed upon will be erroneously included in the  thickness measurement. This error can be eliminated by using two capacitive  sensors to measure a single part. Capacitive sensors are placed on either side  of the part to be measured. By measuring the parts from both sides, curvature  and deformities are taken into account in the measurementand their  effects are not included in the thickness readings.</p>
				        <h4><font color="#000000">D.Non-conductive  targets: </font></h4>
				        <p>While capacitive displacement sensors are most  often used to sense changes in position of conductive targets, they can also be  used to sense the thickness and/or density of non-conductive targets as well. A  non-conductive object placed in between the probe and conductive target will  have a different dielectric constant than the air in the gap and will therefore  change the Capacitance between probe and target. By analyzing this change in  capacitance, the thickness and density of the non-conductor can be determined.</p>
				        <h4><font color="#000000">E.Machine tool  metrology:-</font></h4>
				        <p>Capacitive displacement sensors are often used in  metrology applications. In many cases, sensors are used “to measure shape  errors in the part being produced. But they also can measure the errors arising  in the equipment used to manufacture the part, a practice known as machine tool  metrology”. In many cases, the sensors are used to analyze and optimize the  rotation of spindles in various machine tools, examples include surface  grinders, lathes, milling  machines, and air bearing spindles. By measuring errors in the  machines themselves, rather than simply measuring errors in the final products,  problems can be dealt with and fixed earlier in the manufacturing process</p>
				        <h4><font color="#000000">F.Assembly line testing: -</font></h4>
				        <p>Capacitive displacement sensors are often used in  assembly line testing. Sometimes they are used to test assembled parts for uniformity, thickness or  other design features. At other times, they are used to simply look for the  presence or absence of a certain component, such as glue. Using capacitive  sensors to test assembly line parts can help to prevent quality concerns  further along in the production process.</p>
				        
