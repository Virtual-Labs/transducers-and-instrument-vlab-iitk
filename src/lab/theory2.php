			  <div class="entry">
					 <p>A spectrum analyzer or spectral analyzer is a device used to examine the spectral composition of some electrical, acoustic, or optical waveform. It may also measure the power spectrum.   </p><p>                     
					 <h3 class="style4"><font color="#000000">  </font></h3></p>
					 <p>* It is very important that the correct analyzer be chosen for a given job. There are two basic types of spectrum analyzers: analog and digital. Each has its advantages and disadvantages, and the choice must be determined by the parameters of the job<br>
					 </p><p> *	An analog spectrum analyzer uses either a variable band pass filter whose mid-frequency is automatically tuned (shifted, swept) through the range of frequencies of which the spectrum is to be measured or superheterodyne reciever where the local oscillator is swept through a range of frequencies.<br> </p>
	 <p> *	A digital spectrum analyzer computes the discrete fourier transform (DFT), a mathematical process that transforms a waveform into the components of its frequency spectrum.<br> </p>
	<p>Some spectrum analyzers (such as "real-time spectrum analyzers") use a hybrid technique where the incoming signal is first down converted to a lower frequency using superheterodyne techniques and then analyzed using fast fourier transform (FFT) techniques. An analog analyzer can work with higher frequencies than a digital analyzer. When used with radio frequency circuitry, a spectrum analyzer can measure the noise, frequency of response, signal-to-noise ratio, and distortion inherent in a radio frequency. A common industrial use of this type of analyzer is found in the telecommunications field. They are used in this field to track interference and determine bandwidth.<br><br>
Spectrum analyzers can be used in almost any modern signaling process. Optical analyzers can be used to measure the output of light-emitting devices, such as lasers or diodes. An optical spectrum analyzer can be set to measure the light beams that are emitted.<br><br>
Much can be learned about electrical signals through the use of an oscilloscope; however, an oscilloscope only shows how the signal varies with time. A spectrum analyzer can measure that, and give a graphical representation of the amplitude of the signal frequency. A multitude of measurements can be determined by a spectral analyzer that an oscilloscope cannot measure. These include such things as signal stability and power output, various distortions, and bandwidth.<br><br>
Here we will create a LabVIEW virtual instrument (VI) that generates a waveform (sawtooth,square,triangle,sin manually or any of the desired waveform through DAQ ), uses one of the LabVIEW analysis functions to calculate the power spectrum of the signal with a Fast Fourier Transform (FFT), and creates a plot of the frequency spectrum.
