				<div class="entry">
					<p>The purpose of Instrumentation Lab is to learn about some of the basic instrumentation used to operate modern test and measurement systems. Such systems comprise a mixture of analog electronic devices, like sensors and actuators, with digital data acquisition and control. An analog device is one that uses the level of an electrical signal (often the voltage) to represent the measured value of a physical quantity being observed (say displacement or temperature), or being applied by an actuator (say force or movement). A good example of such a device is a pressure transducer. The transducer has a diaphragm that moves when the pressure changes. The diaphragm is wired up as a part of an electrical circuit. When the circuit is supplied with power (voltage from an electrical power supply) the transducer provides an output voltage that is proportional to the pressure. Like the pressure transducer, all sensors and most actuators are analog devices.a digital device (usually a computer) is one in which the physical quantity is represented as a number. To make a modern measurement or control system work, we need devices that can convert the analog voltage signal produced by a transducer to a series of digital numbers, and that can convert a similar string of numbers back into the voltage signal needed by an actuator. </p>
