Thermistors 
Thermistor is a thermally sensitive resistor that exhibits a change in electrical resistance with a change in its temperature. The resistance is measured by passing a small, measured direct current (dc) through it and measuring the voltage drop produced. Unlike metals, thermistors respond inversely to temperature, i.e., their resistance decreases as the temperature increases. The thermistors are usually composed of oxides of manganese, nickel, cobalt, copper and several other nonmetals.  All measurements made with thermistors can be reduced to detecting the resistance changes, the thermistor must be placed in a circuit and the resistance changes recorded in terms of the corresponding voltage or current changes. We will plung our thermistor into water bath, which is approximately 273 K (0 Â°C) and measure the resistance across the leads and see how close the measured value of R is to the theoretical value. 

