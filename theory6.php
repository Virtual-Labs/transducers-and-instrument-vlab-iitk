				  <br>
				  <p>Strain gages are sensing devices used in a variety of physical test and measurement applications. They change resistance at their output terminals when stretched or compressed. Because of this characteristic, the gages typically are bonded to the surface of a solid material and are used to measure its minute dimensional changes when put in compression or tension.<br>

Strain gages and strain gage principles often are used in devices for measuring acceleration, pressure, tension, and force. Strain is a dimensionless unit, defined as a change in length per unit length. For example, if a 1-m long bar stretches to 1.000002 m, the strain is 2 microstrains.<br></p>
<h3 class="style4"><font color="#000000">Sensor Calibration:   </font></h3><p><P>Every sensor on a machine contributes to the accuracy of the machine or process of operation.
Therefore, an analog sensor that is providing improper information will cause whatever is being
manufactured to be done incorrectly. Further, since some sensors are used for data collection, the
records of the manufacturing process will be incorrect as well.<br>
Once the characteristics of the sensor have been established, preliminary sensor calibration can
proceed. The sensor can be connected into an electrical test circuit or a test bench.<br>
After the sensor is connected and the circuit is determined to be electrically valid, the next
challenge is to find an accurate reference source. If, for example, the sensor is a pressure
transducer with a range of 200 pounds per square inch, then a source of pressure will need to be
provided. Further, the source must be accurate enough and adjustable enough to ensure that the
proper reference can be given to the sensor.<br>
Continuing with the example of the pressure transducer, we will need a variable source of
pressure capable of delivering 0-200 psi with a percentage of accuracy equal to that of the
sensor.<br>
An alternative for most types of sensors is to have several fixed-calibration standards. These
standards would deliver a single point of exact measurement. By having one at a low point on the
sensor range and one at the high point of the sensor range, the sensor can be calibrated to operate
in a linear region using this simple two point calibration. To return to our example, if we are
measuring O-200 psi with our pressure transducer, we would perhaps want a fixed standard at 10 or 20 lbs. and a fixed standard at something toward the high end of the sensor range, perhaps
even exactly 200 lbs.<br>
Because the accuracy of the measurement source is so important, many machine builders and
maintenance departments often choose to send sensors for calibration to certified calibration
labs. It is typically advantageous to perform these repairs and calibrations in-house. There are
several applications however, which require the calibrations to be performed by a certified
authority, for example the aviation and the pharmaceutical industries. The ability to perform
general calibration checking is important in order to determine which sensors may need more
detailed maintenance or whether other items on the machine are in need of maintenance.<br>
Once the sensors are electrically connected into the test circuits and measurement standards, the
next work is done with a calculator. Using the range of the sensor and the maximum signal of the
electrical output, the amount of measured electrical signal can be calculated. (See the equations
at left for voltage or current sample calculations.)<br>
Once this calculation is done, the next step is to apply a measured standard amount to the sensor
and confirm that the sensor provides the electrical output to match the portion of the measured
range. Using our pressure transducer example, if we were to apply 100 lbs. to the transducer, we
would expect to see half of the electrical signal output assuming linear operation.</P><br>
<h3 class="style4"><font color="#000000">Setting zero and span   </font></h3><p><P>If calibration is being performed, the first step is to set the zero point on the transducer. Using the
calibrated measurement standard of zero, you would then adjust the zero adjustment on the
sensor to provide the zero electrical signals from the sensor.<br>
After this is accomplished, the measurement standard should be adjusted to provide full scale, or
100%, of range, and the span adjustment should be set to provide the maximum electrical signal.
If you are unable to provide a maximum signal range, you can adjust the measurement standard
to the maximum you have available and then mathematically calculate the difference from the
maximum electrical range, and then proceed to adjust the span to that amount.<br>
This process of setting a calculated span may be necessary, but it will also be less accurate. The
closer the measurement standard can be set to the sensor "full" range, the more accurate this
process will be. Obviously, this also depends upon the sensor being linear across its range. After
the zero and span have been set, it is usually wise to repeat the settings several times to make
sure that they are repeatable.<br>
Having a written calibration procedure for each type of sensor helps provide a record of
calibration requirements and history. If significant or frequent calibrations are required, there
may be other issues with the sensor that should be investigated.</P><br>

