				<h1 class="title">Introduction </h1>
				
			  <div class="entry">
					 <p>A sensor is a device that produces a measurable response to a change in a physical condition, such as temperature or thermal conductivity, or to a change in chemical concentration. Sensors are particularly useful for making in situ measurements such as in industrial process control. Sensors are an important part to any measurement and automation application. The sensor is responsible for converting some type of physical phenomenon into a quantity measurable by a data acquisition (DAQ) system. Here in this experiment we are dealing with temperature sensors mainly .Several temperature sensing techniques are currently in widespread usage. The most common of these are RTDs, thermocouples, thermistor, and sensor ICs. The right one for our application depends on the required temperature range, linearity, accuracy, cost, features, and ease of designing the necessary support circuitry. </p>
			<h3 class="style4"><font color="#000000">RTDs  </font></h3><p>The resistance of a conductor changes when its temperature is changed. This property is utilized for measurement of temperature.<br>
	The variation of resistance R with temperature T( <sup>0</sup>K) can be represented by the following relationship for most of the metals as:
		<div align="center">	R = R<sub>0</sub> (1+ a<sub>1</sub>T+ a<sub>2</sub>T<sup>2</sup>+....+a<sub>n</sub>T<sup>n</sup>+....)</div>
			<p> 
			  where R<sub>0</sub> = resistance at temperature T =0 and a<sub>1</sub>, a<sub>2</sub>, a<sub>3</sub>....a<sub>n</sub> are 
			  constants. 
			  The resistance thermometer used the change in electrical resistance of conductor to determine the temperature.<br> 
	The resistivity of metals showed a marked dependence on temperature, which was discovered by Sir Humphry Davy. A few years later Sir William Siemens preferred the use of platinum as an element in the resistance thermometer. His choice proved most propitious, as platinum is used to this day as the primary element in all high accuracy resistance thermometers. In fact, the platinum resistance temperature detector (PRTD) is used today as an interpolation standard from oxygen point (-182.96<sup>0</sup>C) to antimony point (630.74<sup>0</sup>C).<br> <br>
	Platinum is especially suited for this purpose, as it can withstand high temperatures while maintaining excellent stability. As a noble metal, it shows limited susceptibility to contamination. All metals produce a positive change in resistance with temperature. This, of course, is the main function of an RTD. The system error is minimized when the nominal value of RTD is large. This implies a metal with a high value of resistivity should be used for RTDs. The lower is the resistivity of the metal, the more material we will have to use.<br> 
	The requirements of a conductor material to be used in RTDs are: <br>
<b>(i)</b>	The change in resistance of material per unit change in temperature should be as large as possible. <br>
<b>(ii)</b>	The material should have a high value of resistivity so that minimum volume of material is used for the construction of RTD. <br>
<b>(iii)</b>	The resistance of materials should have a continuous and stable relationship with temperature. <br>

 Gold and Silver are rarely used for construction of RTDs on account of their low resistivities. Tungsten has a relatively  high resistivity, but is reserved for high temperature applications as it is extremely brittle and difficult to work. Copper is used occasionally as an RTD element. Its low resistivity forces the element to be longer than the platinum element, but its low linearity and low cost make it an economical alternative. Its upper limit of temperature is about 120<sup>0</sup>C.<br> 
	The most common RTDs are made of either platinum, nickel or nickel alloys. The economical nickel wires are used over a limited temperature range. They are quite non-linear and tend to drift with time. For measurement integrity, platinum is the obvious choice. <br>
	The common values of resistance for a platinum RTD range from 10W for the bird cage model to several thousands ohm for the film RTD. The single most common value is 100W at 0<sup>0</sup>C  with a resistance temperature co-efficient of 0.00385/ <sup>0</sup>C . 
	The more chemically pure platinum wire has a resistance temperature co-efficient of 0.00392/ <sup>0</sup>C <br>
	
	
