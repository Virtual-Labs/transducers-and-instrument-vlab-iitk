	<h3 class="style4"><font color="#000000">Thermistor</font></h3><br><p>Thermistors are special solid temperature sensors that behave like temperature-sensitive resistors. Like the RTD, the thermistor is also a temperature sensitive resistor. The word that best describes the thermistor is sensitive. Of the three major categories of sensors, the thermistor exhibits by far the largest parameter change with temperature. Thermistors are generally composed of semiconductor materials. Although positive temperature coefficient units are available, most thermistors have a negative temperature coefficient (TC); that is, their resistance decreases with increasing temperature. The negative T.C. can be as large as several percent per degree Celsius, allowing the thermistor circuit to detect minute changes in temperature which could not be observed with an RTD or thermocouple circuit. The price we pay for this increased sensitivity is loss of linearity. The thermistor is an extremely non-linear device which is highly dependent upon process parameters. Consequently, manufacturers have not standardized thermistor curves to the extent that RTD and thermocouple curves have been standardized.
R=1/T.
<br><h3 class="style4"><font color="#000000">Thermocouple</font></h3><br><p>A thermocouple consists of two dissimilar metals, joined together at one end, and produce a small unique voltage at a given temperature. This voltage is measured and interpreted by a thermocouple thermometer.
The thermoelectric voltage resulting from the temperature difference from one end of the wire to the other is actually the sum of all the voltage differences along the wire from end to end. Thermocouples can be made from a variety of metals and cover a temperature range 200 oC to 2,600 oC. It is Capable of being used to directly measure temperatures up to 2600 oC.The thermocouple junction may be grounded and brought into direct contact with the material being measured
V=f(T) where V is the seeback voltage, f is the seeback coefficient and T is the temperature. 

</p>

