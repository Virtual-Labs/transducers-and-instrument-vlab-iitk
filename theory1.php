				  <p>Here we have used three temperature sensors namely RTD, thermistor and Thermocouple. Each temperature sensor has its own unique properties and dependence on temperature according to their types and materials used to make them. The first sensor which we examine is RTD.<br>
				<h3 class="style4"><font color="#000000">RTD</font></h3><br><p>Resistance Temperature Detectors (RTD), as the name implies, are sensors used to measure temperature by correlating the resistance of the RTD element with temperature. Most RTD elements consist of a length of fine coiled wire wrapped around a ceramic or glass core. The element is usually quite fragile, so it is often placed inside a sheathed probe to protect it. The RTD element is made from a pure material whose resistance at various temperatures has been documented. The material has a predictable change in resistance as the temperature changes; it is this predictable change that is used to determine temperature.<br>
RTDs are commonly categorized by their nominal resistance at 0 °C. Typical nominal resistance values for platinum thin-film RTDs include 100 Ω and 1000 Ω. The relationship between resistance and temperature is very nearly linear and follows the equation.                
				<div align="center"><br>For<0<sup>o</sup>C R<sub>T</sub>=R<sub>o</sub>[1+aT+bT<Sup>2</Sup>+cT<sup>3</sup>(T-100)]
				<br>For>0<sup>o</sup>C R<sub>T</sub>=R<sub>o</sub>[1+aT+bT<Sup>2</Sup>]		</div>		
