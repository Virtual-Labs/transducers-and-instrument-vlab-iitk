				<h3 class="title"><div align="left">Swept Spectrum Analyzer</div></h3>
				
			  <div class="entry">
					 <p>The swept spectrum is based on a different configuration than the FFT analyzer. It uses what is termed as a super-heterodyne configuration. Heterodyne refers to mixing and super refers to super-audio frequencies, or frequencies above the audio range. Similar to the FFT analyzer, the swept spectrum analyzer uses an attenuator and gain stages to adjust the signal to fit the input range of the analyzer. A voltage-controlled oscillator (VCO) sweeps through a range of frequencies that are mixed with the incoming signal. The signal from the input and the signal from the VCO are passed through a mixer. A mixer is a nonlinear device that produces the sum and difference of the original signal and the signal from the VCO, as well as the original signals and their harmonics. An intermediate frequency (IF) filter extracts the desired sum or difference of the original signals. The detector produces a voltage level relative to the amount of power received from the incoming signal. As the VCO sweeps different frequencies, the detector produces a corresponding voltage level, or power measurement.<br>

An important parameter of the swept analyzer is resolution bandwidth. It is determined by the IF filter portion below the minimum insertion-loss point. Often the IF filter section is composed of a number of filters, which together determine the resolution bandwidth. Resolution bandwidth is affected by the speed at which the instrument sweeps the frequency range. The IF filter requires a certain time to respond to signals placed at the input, so the analyzer cannot sweep through the frequency range too fast. Two errors can result from sweeping through the frequency range too quickly. First, the amplitude is at a lower level than if it were swept at a slower speed. Second, the signal shifts up in frequency.<br>

Because any time involved in sweeping the frequency range causes errors in the signal due to the filter response time, the instrument should sweep infinitely slow. However, this is not practical, so an acceptable error must be determined from which the sweep rate can be calculated. The following equation is used to calculate the maximum sweep rate according to the resolution bandwidth (RBW)<br>
<h3 class="style4"><font color="#000000">Comparison</font></h3><br><p>
One of the principal advantages that the swept analyzer has over the FFT analyzer is its frequency range. Because the FFT analyzer needs to acquire a digital time domain waveform, the frequency range of the FFT analyzer is dependent on the sampling rate of the ADC. The sampling rate of the analyzer determines the highest frequency that can be analyzed with the FFT. Current ADC technology has brought sampling rates into the MHz range. Swept spectrum analyzers, on the other hand, can measure frequencies in the high GHz range.<br>

A key advantage of the FFT analyzer over the swept spectrum analyzer is its speed. Because the sweep rate is proportional to the resolution bandwidth squared, the sweep rate is decreased for smaller resolution bandwidths. This increases the total measurement time. The FFT analyzer is only limited in time by how long it takes to acquire the data and to compute the FFT.<br>

In order to analyze signals that change in frequency over time, it is important to acquire data fast enough to view the changes in frequency. An example of this type of signal is with transient analysis. Swept spectrum analyzers are only capable of detecting continuous wave (CW) signals. The length of time it takes to complete a frequency sweep on the swept spectrum analyzer makes it inappropriate for measuring fast changing signals at lower frequencies. The FFT analyzer is much faster than the swept spectrum analyzer in acquiring spectrums, especially if the analyzer has dedicated DSP to perform the FFT. If the FFT analyzer is a real-time system, then you can measure the signall without losing any of the frequency data over time. For this reason, the FFT analyzer is a better choice for acquiring signals that change with frequency.<br>

If phase information is desired, you must use the FFT analyzer. The FFT analyzer is based on the Fourier algorithm, which includes both real and imaginary numbers. From these numbers, you can calculate the magnitude and phase at each frequency. The swept spectrum analyzer can only acquire magnitude information for the user.




  </p>
					
					 <br>                     
					 <h3 class="style4"><font color="#000000">Conclusion</font></h3><br><p>
					 It is important to consider how the properties of each instrument will affect the measurements when choosing an instrument for high-frequency spectrum analysis. Making high-frequency measurements requires an instrument that has an appropriate frequency range for the signal. FFT analyzers have a lower frequency range than swept spectrum analyzers, making swept analyzers the better choice for very high-frequency ranges. If the length of measurement time is critical, such as with transient analysis, then the FFT analyzer is the better choice because of its speed. Because the swept spectrum analyzer does not provide phase information, the FFT analyzer must be selected if this information is desired.
