				<h3 class="style4"><font color="#000000">Thermistors  </font></h3><p>
					 <p>Thermistor is a contraction of a term "thermal resistors". Thermistors are generally composed of semi-conductor materials. Although positive temperature co-efficient of units (which exhibit an increase in the value of resistance with increase in temperature) are available, most thermistors have a negative coefficient of temperature resistance i.e., their resistance decreases with increase of temperature. The negative temperature coefficient of resistance can be as large as several percent per degree celcius. This allows the thermistor circuits to detect very small changes in temperature which could not be observed with an RTD or a thermocouple. In some cases the resistance of thermistor at room temperature may decrease as much as 5 percent for each 1<sup>0</sup>C rise in temperature. This high sensitivity to temperature changes makes thermistors extremely useful for precision temperature measurements control and compensation. Thermistors are widely used in applications which involve measurements in the range of -60<sup>0</sup>C to 15<sup>0</sup>C . The resistance of thermistors ranges from 0.5W to 0.75MW  . Thermistor is a highly sensitive device. The price to be paid off for the high sensitivity is in terms of linearity. The thermistor exhibits a highly non linear characteristic of resistance versus temperature. 
 </p>
			<h3 class="style4"><font color="#000000">Construction of Thermistors  </font></h3><p>
			<P> Thermistors are composed of sintered mixture of metallic oxides such as manganese, nickel, cobalt, copper, iron and uranium. They are available in variety of sizes and shapes. The thermistors may be in the form of beads, rods and discs. Some of the commercial forms are shown in Fig below. 

 </p>
			<P><div align="center"><img src="images/f3.JPG" alt="" width="616" height="334" /></div></p>
			<div align="center"><font color="#000000">Figure shows: Different forms of construction of thermistors</font></div><br>
			<p>A thermistor in the form of a bead is smallest in size and the bead may have a diameter of 0.015 mm to 1.25mm. Beads may be sealed in the tips of solid glass rods to form probes which may be easier to mount than the beads. Glass probes have diameter of 2.5 mm and a length which varies from 6 mm to 50 mm. Discs are made by pressing material under high pressure into cylindrical flat shapes with diameters ranging from 2.5 mm to 25 mm.</p>
